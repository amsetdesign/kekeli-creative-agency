- 1 clip ou contenu long par mois sur YouTube

# 3. Produire et distribuer votre musique

## La production : ne sacrifiez pas la qualité

Un bon morceau mal produit n''ira nulle part. Investissez dans un bon studio à Dakar. Les tarifs varient entre **15 000 et 80 000 FCFA par session** selon le studio. Privilégiez un ingénieur du son qui connaît votre genre.

## La distribution digitale : soyez partout

Pour que votre musique soit sur Spotify, Apple Music, Boomplay, YouTube Music et les autres plateformes, vous avez besoin d''un distributeur. Les options disponibles pour les artistes sénégalais :

- **DistroKid** : le moins cher (environ 20$/an pour des sorties illimitées)
- **TuneCore** : payant par sortie mais bon suivi des royalties
- **Boomplay Africa** : idéal pour la distribution spécifiquement africaine
- **KEKELI Creative Agency** : nous gérons la distribution pour vous, avec suivi et optimisation inclus

## L''ISRC : votre numéro d''identité musical

Chaque morceau distribué doit avoir un **code ISRC** (International Standard Recording Code). Ce code unique permet de tracer vos écoutes et de percevoir vos royalties. Ne le négligez pas.

# 4. Le droit d''auteur et la BSDA

En tant qu''artiste sénégalais, vous devez obligatoirement vous enregistrer auprès du **Bureau Sénégalais du Droit d''Auteur (BSDA)**. C''est gratuit et indispensable pour :

- Percevoir vos droits d''auteur sur les diffusions radio et TV
- Être protégé légalement contre le plagiat
- Toucher vos royalties des plateformes de streaming

## Les démarches pratiques

Rendez-vous au siège de la BSDA à Dakar avec votre pièce d''identité, vos morceaux enregistrés et les textes des paroles. L''enregistrement est simple mais souvent négligé par les artistes débutants — ce qui leur coûte des revenus importants par la suite.

# 5. Le booking et les concerts

## Construire votre dossier artiste

Avant de démarcher des promoteurs ou organisateurs d''événements, vous avez besoin d''un **dossier artiste professionnel** contenant :

- Biographie courte et longue
- Lien vers vos meilleures vidéos live
- Photos professionnelles haute définition
- Fiche technique (rider son et lumière)
- Tarifs de prestation (même approximatifs)

## Les premiers concerts

Ne visez pas les grandes scènes tout de suite. Commencez par :

- Les soirées culturelles dans les centres culturels
- Les événements communautaires et universitaires
- Les plateformes comme les open mics et battles
- Les festivals régionaux hors de Dakar

Chaque performance est une opportunité de capter des fans, des vidéos et des contacts.

