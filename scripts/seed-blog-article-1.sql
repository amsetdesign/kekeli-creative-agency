# 1. Construire votre identité artistique

Avant de poster un seul contenu ou de sortir votre premier morceau, vous avez besoin d''une chose essentielle : **savoir qui vous êtes en tant qu''artiste**.

L''identité artistique, c''est l''ensemble des éléments qui vous rendent immédiatement reconnaissable :

- Votre nom d''artiste (mémorable, prononçable, disponible sur les réseaux)
- Votre univers visuel (couleurs, style de photos, typographies)
- Votre genre musical et votre "son" distinctif
- Votre message, vos valeurs, ce que vous défendez

## Pourquoi c''est la base de tout

Les artistes qui percent ne sont pas forcément les plus talentueux — ce sont ceux qui ont l''identité la plus claire. Un fan qui voit votre contenu doit vous reconnaître instantanément, même sans voir votre nom.

## L''exercice à faire aujourd''hui

Complétez cette phrase : "Je suis [nom d''artiste], je fais de la [genre] à [ville], je parle de [thème] pour [audience]." Si vous ne pouvez pas la remplir en 10 secondes, votre identité n''est pas encore définie. C''est le moment d''y travailler.

Un **logo professionnel**, une **charte graphique** cohérente et un **press kit** (EPK) bien conçu sont les outils qui transforment un artiste amateur en artiste professionnel.

# 2. Soigner votre présence digitale

En 2026, votre présence en ligne est votre première carte de visite. Avant même d''entendre votre musique, les bookers, labels et fans vont vous chercher sur Instagram, TikTok et YouTube.

## Les plateformes prioritaires au Sénégal

- **Instagram** : vitrine principale, photos, reels et stories. Minimum 3 publications par semaine
- **TikTok** : croissance organique la plus forte actuellement. Un seul clip peut vous faire gagner 50 000 abonnés en une nuit
- **YouTube** : indispensable pour les clips et la monétisation long terme
- **Facebook** : toujours pertinent en Afrique de l''Ouest, surtout pour les 30 ans et plus

## Ce que vous devez publier

Ne pensez pas seulement à promouvoir votre musique. Montrez votre quotidien en studio, vos sessions de création, vos répétitions, vos moments derrière le rideau. **Les artistes qui construisent une communauté, pas juste un public, durent.**

### Fréquence recommandée pour débuter
- 1 reel ou tiktok par jour (ou 5 par semaine minimum)
